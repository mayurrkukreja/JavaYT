
public class StaticPrint {
    // Print on new Line function :
    public static void println(String str) {
        System.out.println(str);
    }

    // Print on same Line function :
    public static void print(String str1) {
        System.out.print(str1);
    }

    //Main Function :
    public static void main(String[] args) {
        println("Hello World!");
        println("Programming isn't about what you know,");
        print("It's about what you can figure out!");
    }
}


